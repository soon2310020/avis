DROP TABLE IF EXISTS `AI_QUALITY_MANAGEMENT`;
DROP TABLE IF EXISTS `AI_PQ_RESULT`;

CREATE TABLE AI_PQ_RESULT (
    ID BIGINT NOT NULL AUTO_INCREMENT,
	STATISTICS_ID BIGINT(19) NOT NULL,
	MOLD_ID BIGINT(19) NOT NULL,
	COUNTER_ID BIGINT(19) NOT NULL,
	PART_ID BIGINT(19) NOT NULL,
	COMPANY_ID BIGINT(19) NOT NULL,
	CATEGORY_ID BIGINT(19) NOT NULL,
	PARENT_ID BIGINT(19) NOT NULL,
	HOUR VARCHAR(50) NULL DEFAULT NULL ,
	CT_STATUS VARCHAR(50) NULL DEFAULT NULL ,
	TEMP_STATUS VARCHAR(50) NULL DEFAULT NULL ,
	PPA_STATUS VARCHAR(50) NULL DEFAULT NULL ,
	QD_STATUS VARCHAR(50) DEFAULT NULL,
	PQ_RESULT VARCHAR(50) DEFAULT NULL,
	PQ_RESULT_TIME DATETIME NULL DEFAULT NULL,	
	CREATED_AT DATETIME NULL DEFAULT NULL,
	UPDATED_AT DATETIME NULL DEFAULT NULL,
	PRIMARY KEY (ID) USING BTREE,
	UNIQUE INDEX UX_AQM (STATISTICS_ID, MOLD_ID) USING BTREE	
) CHARSET=utf8;
